public  class JavaBasics {

    public static void main(String[] args) {
    // Output in Java
        /*There are three methods for the output in java
        1. print
        2. println
        3. "\n"

    */
        // By print
        System.out.print("Hello World");
        System.out.print("Hello World");
        System.out.print("Hello World");

        // By println
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        // By "\n"
        System.out.print("Hello World\nHello World\nHello World\n");


        // Print Pattern
        // ****
        // ***
        // **
        // *

        System.out.println("Print Pattern \n****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
    }
}
